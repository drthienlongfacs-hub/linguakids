/** Generate flashcard pairs (word + meaning) from a chapter */
export function generateFlashcards(chapter) {
  return chapter.items.map((item, idx) => ({
    id: idx,
    front: item,
    back: item, // TTS will pronounce it
    chapter: chapter.title,
  }));
}

/** Generate listen-and-choose quiz from a chapter */
export function generateListenQuiz(chapter, count = 5) {
  const items = [...chapter.items].sort(() => Math.random() - 0.5);
  const questions = [];
  
  for (let i = 0; i < Math.min(count, items.length); i++) {
    const correct = items[i];
    // Pick 3 wrong answers from same chapter
    const wrongs = items
      .filter((_, j) => j !== i)
      .sort(() => Math.random() - 0.5)
      .slice(0, 3);
    
    const options = [correct, ...wrongs].sort(() => Math.random() - 0.5);
    questions.push({
      id: i,
      audio: correct,
      options,
      answer: correct,
    });
  }
  return questions;
}

/** Generate sentence-building exercise (for sentence_pattern chapters) */
export function generateSentenceBuild(chapter, count = 5) {
  const sentences = chapter.items
    .filter(item => item.includes(' '))
    .sort(() => Math.random() - 0.5)
    .slice(0, count);
  
  return sentences.map((sentence, idx) => {
    const words = sentence.replace(/[.!?]/g, '').split(' ');
    const scrambled = [...words].sort(() => Math.random() - 0.5);
    return {
      id: idx,
      sentence,
      words: scrambled,
      answer: words,
    };
  });
}

/** Get chapter by ID */
export function getChapter(chapterId) {
  return TEACHER_CURRICULUM.chapters.find(c => c.id === chapterId);
}

/** Get chapters by category */
export function getChaptersByCategory(category) {
  return TEACHER_CURRICULUM.chapters.filter(c => c.category === category);
}

