/** Generate flashcard pairs from a chapter */
export function generateFlashcards(chapter) {
  return chapter.items.map((item, idx) => ({
    id: idx,
    en: item.en,
    vi: item.vi,
    chapter: chapter.title,
  }));
}

/** Generate listen-and-choose quiz */
export function generateListenQuiz(chapter, count = 8) {
  const items = [...chapter.items].filter(i => i.en.length > 2).sort(() => Math.random() - 0.5);
  const questions = [];
  for (let i = 0; i < Math.min(count, items.length); i++) {
    const correct = items[i];
    const wrongs = items.filter((_, j) => j !== i).sort(() => Math.random() - 0.5).slice(0, 3);
    const options = [correct, ...wrongs].sort(() => Math.random() - 0.5);
    questions.push({
      id: i,
      audio: correct.en,
      audioVi: correct.vi,
      options: options.map(o => o.en),
      optionsVi: options.map(o => o.vi),
      answer: correct.en,
      answerVi: correct.vi,
    });
  }
  return questions;
}

/** Generate sentence-building exercise */
export function generateSentenceBuild(chapter, count = 6) {
  const sentences = chapter.items
    .filter(item => item.en.includes(' ') && item.en.split(' ').length >= 3)
    .sort(() => Math.random() - 0.5)
    .slice(0, count);
  return sentences.map((item, idx) => {
    const words = item.en.replace(/[.!?]/g, '').split(' ');
    const scrambled = [...words].sort(() => Math.random() - 0.5);
    return {
      id: idx,
      sentence: item.en,
      sentenceVi: item.vi,
      words: scrambled,
      answer: words,
    };
  });
}

/** Get chapter by ID */
export function getChapter(chapterId) {
  return TEACHER_CURRICULUM.chapters.find(c => c.id === chapterId);
}

/** Get chapters by category */
export function getChaptersByCategory(category) {
  return TEACHER_CURRICULUM.chapters.filter(c => c.category === category);
}

